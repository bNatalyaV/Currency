import Foundation
import UIKit

extension UIColor {
    static func baseBackgroundColor() -> UIColor {
        return .white
    }
    
    static func mainColor() -> UIColor {
        return UIColor(red: 0.481, green: 0.038, blue: 0.829, alpha: 1)
    }
}
